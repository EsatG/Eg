package day18_nestedLoops;

public class DoWhileLoop2 {
    public static void main(String[] args) {

        int number = 10;

        while ( number >= 100) {
            System.out.println(number);
            number--;
        }
        System.out.println("==============================================================");

        int number2 = 10;
        do {
            System.out.println(number2);
            number2--;
        } while ( number2 >= 100);







    }
}
